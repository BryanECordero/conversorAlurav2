    private void initCombos() {
        for(int i = 0; i < monedas.length; i++){
             fromMonedaCombo.addItem(monedas[i]);
             toMonedaCombo.addItem(monedas[i]);
             mElementos.put(monedas[i], getIcono(banderas[i]));
        }
        
        jComboBoxRender mRender = new jComboBoxRender(mElementos);
        fromMonedaCombo.setRenderer(mRender);
        toMonedaCombo.setRenderer(mRender);
        toMonedaCombo.setSelectedIndex(1);
        Monedas.actulizarTasas();
        updateLabel.setText("Ãšltima actualizacion de tasas: " + Monedas.getFechaActualizacion());
        
        for(String t: temps){
            fromTempCombo.addItem(t);
            toTempCombo.addItem(t);
        }
        toTempCombo.setSelectedIndex(1);
        
        for(String t: masas){
            fromMasaCombo.addItem(t);
            toMasaCombo.addItem(t);
        }
        toMasaCombo.setSelectedIndex(1);
        
        for(String t: dist){
            fromDistanciaCombo.addItem(t);
            toDistanciaCombo.addItem(t);
        }
        toDistanciaCombo.setSelectedIndex(1);
    }
    
